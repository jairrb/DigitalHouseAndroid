package br.com.digitalhouse.Aula5.exercicios.exercicio5;

public class Carro {
    private String marca;
    private String modelo;
    private String cor;
    private int fabricacao;
    private int quilometragem;

    public Carro(String marca, String modelo, String cor, int fabricacao, int quilometragem) {
        this.marca = marca;
        this.modelo = modelo;
        this.cor = cor;
        this.fabricacao = fabricacao;
        this.quilometragem = quilometragem;
    }
}
